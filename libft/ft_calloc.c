#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*new;

	if ((nmemb != 0 && size != 0) && nmemb > INT_MAX / size)
		return (NULL);
	new = malloc(nmemb * size);
	if (!new)
		return (NULL);
	ft_bzero(new, nmemb * size);
	return (new);
}
